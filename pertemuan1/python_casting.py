#Casting to Integer
a = int(8)   # a akan menjadi 8
b = int(-9.880) # b akan menjadi -9
c = int("-1") # c akan menjadi -1
print(a)
print(type(a))
print(b)
print(type(b))
print(c)
print(type(c))

#Casting to Float
d = float(1-10)  # d akan menjadi -9.0
e = float(12.880)   # e akan menjadi 12.88
f = float("12")   # f akan menjadi 12.0
g = float("-133.9") # g akan menjadi -133.9

print(d)
print(type(d))
print(e)
print(type(e))
print(f)
print(type(f))
print(g)
print(type(g))

#Casting to string
h = str("sumedho") # h akan menjadi 'sumedho'
i = str(-2)    # i akan menjadi '-2'
j = str(32.99)  # j akan menjadi '32.99'
k = str(32.9e3) # k akan menjadi '32900.0'

print(h)
print(type(h))
print(i)
print(type(i))
print(j)
print(type(j))
print(k)
print(type(k))